// 标签统计
function getAllTags() {
  const tagMap = {};
  ARTICLES.forEach(article => {
    article.tags.forEach(tag => {
      if (tagMap[tag]) {
        tagMap[tag]++;
      } else {
        tagMap[tag] = 1;
      }
    });
  });
  return Object.entries(tagMap)
    .sort((a, b) => b[1] - a[1])
    .map(([name, count]) => ({ name, count }));
}

